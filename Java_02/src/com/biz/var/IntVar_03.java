package com.biz.var;

public class IntVar_03 {
	
	/*
	 * method : 일종의 개발자가 만든 키워드
				명령문들의 모음
	main(String[] args) -> main() method라고 호칭 */
	public static void main(String[] args) {
		// num1이라는 integer형 변수로 4Byte만큼 메모리 공간을 할당해서 30을 대입
		int num1=30;
		// 지역변수, 전역변수 개념
		System.out.println(num1);
		
	}
	//여기서 num1을 사용하면 할당되지 않은, 없는 변수라고 나온다. 지역변수
}
