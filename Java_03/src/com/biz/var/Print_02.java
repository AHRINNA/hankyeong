package com.biz.var;

public class Print_02 {
	public static void main(String[] args) {
		
		int num1=30, num2=40;
		
		System.out.println(num1 + " + " + num2 + " = " + num1 + num2);
		System.out.print(num1 + " + " + num2 + " = " + num1 + num2);
		// c언어 printf(%d" % "%d" = "%d, num1, num2, num1%num2);
		// Java의 +는 문자열 연결에 쓰일 수 있다. 이 경우엔 연산자로 쓰이지 않는다
		// 문자열이 하나라도 있으면 모든 값들을 문자열로 변환시키고
		// 변환된 문자열들을 연결해서 콘솔로 보낸다
		// 문자열에서 연산자로 쓰려면 ()로 묶어야한다. 묶으면 결과값은 ""로 들어간다(문자열로)
		// Java는 선언문과 연산문을 한 문장에 쓸 수 있다
		
	}
}
