package com.biz.loop;

public class Loop_12 {
	public static void main(String[] args) {
		
		for(int i = 0 ; i < 10; i++) {
			for(int j = 0; j< 10; j++) {
				System.out.print(j + " ");				
			}
			System.out.println();
		}
		System.out.println("===================");
			
			for(int i = 0 ; i < 10; i++) {
					for(int j = 0; j< 10; j++) {
						System.out.print("* ");				
					}
				System.out.println();
		}
			/* 2중 for 반복문
			 * 2개 이상의 반복문을 중첩시켜 실행하는 구조
			 */
	}
}
